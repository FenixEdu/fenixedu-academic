DROP TABLE IF EXISTS `CMS_WEBSITE_ITEM`;
CREATE TABLE `CMS_WEBSITE_ITEM` (
	`ID_INTERNAL` int NOT NULL auto_increment, 
	`NAME` text,
	`URGENT` int NOT NULL DEFAULT 0,
	`DESCRIPTION` text,
	`CREATION_DATE` timestamp NOT NULL,
	`KEY_CREATOR` int NOT NULL,
    `KEY_CMS` int NOT NULL,
	`WRITERS_GROUP` BLOB,
	`READERS_GROUP` BLOB,	
	PRIMARY KEY (`ID_INTERNAL`)
)ENGINE=InnoDB;

DROP TABLE IF EXISTS `CMS_WEBSITE_SECTION`;
CREATE TABLE `CMS_WEBSITE_SECTION` (
	`ID_INTERNAL` int NOT NULL auto_increment,
    `CREATION_DATE` timestamp NOT NULL,
    `KEY_CREATOR` int NOT NULL,
    `NAME` text,
    `DESCRIPTION` text,
    `KEY_CMS` int NOT NULL,
	`WRITERS_GROUP` BLOB,
	`READERS_GROUP` BLOB,	
	PRIMARY KEY (`ID_INTERNAL`)
)ENGINE=InnoDB;